const fs = require("fs");
const path = require("path");

const dir = "examples/src/components/docs/";
const files = [];

const getFilesRecursively = (directory) => {
    const filesInDirectory = fs.readdirSync(directory);
    for (const file of filesInDirectory) {
        const absolute = path.join(directory, file);
        if (fs.statSync(absolute).isDirectory()) {
            getFilesRecursively(absolute);
        } else {
            files.push(absolute);
        }
    }
};

getFilesRecursively(dir);

const trimmedFiles = files.map((filePath) =>
    filePath.replace(/\\/g, "/").replace(dir, "")
);

// Embed raw .ts source so docs viewer shows the original TypeScript instead of
// the Ivy-compiled output produced by a dynamic ES import.
const escapeBackticks = (s) => s.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
const tsSources = files
    .map((absolutePath, i) => {
        if (!absolutePath.endsWith(".ts")) return null;
        const relative = trimmedFiles[i];
        const content = fs.readFileSync(absolutePath, "utf8");
        return `    "${relative}": \`${escapeBackticks(content)}\``;
    })
    .filter(Boolean)
    .join(",\n");

fs.writeFileSync(
    `${dir}/demo.files.ts`,
    `// this file autogenerated, do not edit it manually please run the script\n// yarn run compile-demo-paths\nexport const DEMO_PATHS = [\n    ${trimmedFiles
        .map((f) => `"${f}"`)
        .join(",\n    ")},\n];\n\nexport const DEMO_TS_SOURCES: Record<string, string> = {\n${tsSources},\n};\n`
);
